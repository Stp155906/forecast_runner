def find_moon_rule(rules: List[Dict[str,Any]], phase_for_rules: str, sign: str) -> Optional[Dict[str,Any]]:
    phase_for_rules = (phase_for_rules or "").lower()
    sign = tcase(sign)
    best = None
    for r in rules:
        if r.get("type") != "moon_phase": continue
        m = r.get("match", {})
